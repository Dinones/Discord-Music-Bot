###########################################################################################################################
#   Holds the Spotify playlist list loaded from AWS Secrets Manager at startup. Populated once in main().               #
###########################################################################################################################

###########################################################################################################################
####################################################     LIBRARIES     ####################################################
###########################################################################################################################

from __future__ import annotations

import json
from typing import List, Dict, Any

###########################################################################################################################
#################################################     INITIALIZATIONS     #################################################
###########################################################################################################################

_playlists: List[Dict[str, str]] = []

###########################################################################################################################
###########################################################################################################################

def load_playlists(secrets: Dict[str, Any]) -> None:

    """
    Populate the module-level playlist list from the AWS secrets dictionary.
    Accepts the list either as a native Python list or as a JSON-encoded string.

    Args:
        secrets (Dict[str, Any]): Parsed secrets dict returned by get_secrets().

    Returns:
        None
    """

    global _playlists

    raw = secrets.get("SPOTIFY_PLAYLISTS", [])

    if isinstance(raw, str):
        try:
            raw = json.loads(raw)
        except (json.JSONDecodeError, TypeError):
            raw = []

    _playlists = raw if isinstance(raw, list) else []

###########################################################################################################################
###########################################################################################################################

def get_playlists() -> List[Dict[str, str]]:

    """
    Return the loaded playlist list.

    Args:
        None

    Returns:
        List[Dict[str, str]]: List of playlist dicts with "name" and "url" keys.
    """

    return _playlists

###########################################################################################################################
#####################################################     PROGRAM     #####################################################
###########################################################################################################################

if __name__ == "__main__":
    pass

import os
import sys
import logging
import unittest

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "..", "..")))

import Utils.Logs

###########################################################################################################################
#################################################     INITIALIZATIONS     #################################################
###########################################################################################################################

class TestSetDiscordLoggingMessagesLevel(unittest.TestCase):

    def test_sets_level_on_discord_logger(self) -> None:

        Utils.Logs.set_discord_logging_messages_level("WARNING")
        logger = logging.getLogger("discord")

        self.assertEqual(logger.level, logging.WARNING)
        self.assertFalse(logger.disabled)
        self.assertTrue(logger.propagate)

    #######################################################################################################################
    #######################################################################################################################

    def test_disables_discord_logger_when_empty_string(self) -> None:

        Utils.Logs.set_discord_logging_messages_level("")
        logger = logging.getLogger("discord")

        self.assertTrue(logger.disabled)

    #######################################################################################################################
    #######################################################################################################################

    def test_disables_discord_logger_when_invalid_level(self) -> None:

        Utils.Logs.set_discord_logging_messages_level("INVALID_LEVEL")
        logger = logging.getLogger("discord")

        self.assertTrue(logger.disabled)

